# arithmetic_and_comparison.py
x = 10  # You can replace with any values
y = 3

# Arithmetic operations
print(f"x + y = {x + y}")
print(f"x - y = {x - y}")
print(f"x * y = {x * y}")
print(f"x / y = {x / y}")
print(f"x // y = {x // y}")  # Floor division
print(f"x ** y = {x ** y}")  # Exponentiation
print(f"x % y = {x % y}")    # Modulus

# Comparison operations
print(f"x == y: {x == y}")
print(f"x != y: {x != y}")
print(f"x < y: {x < y}")
print(f"x > y: {x > y}")
print(f"x <= y: {x <= y}")
print(f"x >= y: {x >= y}")
